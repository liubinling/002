    private void loadOptText(JComboBox box, String grp) {
        List<String> list = this.opt.getMemList(grp);
        for (String s : list) {
            box.addItem(s);
        }
        if (list.isEmpty()) {
            box.addItem("");
        }
        box.setSelectedIndex(0);
    }

